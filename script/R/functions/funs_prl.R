    ifelse(prl_data$rt < 150 | prl_data$rt > 2499, NA, prl_data$rt)
  
  prl_data$rt1 <- ifelse(prl_data$trial == 1, NA, prl_data$rt1)
  
  # check whether there are patients with NA on diag_cat
  if (0) {
    summary(factor(prl_data$diag_cat))
    subjects_with_na_diag_cat <- prl_data[is.na(prl_data$diag_cat), ]
    summary(factor(subjects_with_na_diag_cat$diag_cat))
