    patients_names_from_excel_df <- rio::import(
      here::here("data", "raw", "misc", "diagn_cat.xlsx")
    )
    patients_names_from_excel <- patients_names_from_excel_df$subj_code
    patients_names_from_excel %in% names_with_na
  }
  # There are no patients in the 37 participants with no questionnaires data.
  # All these 37 participants are then attributed to the HC category.
  prl_data$diag_cat <- ifelse(is.na(prl_data$diag_cat), "HC", prl_data$diag_cat)
  
  prl_data %>% 
    group_by(diag_cat, group) %>% 
    summarise(
      n = n_distinct(subj_name)
